
## Crear un diccionario vacío 
persona = {}

## Añadir pares clave-valor al diccionario
persona['nombre'] = 'Juan'

## Crear un diccionario con valores iniciales
persona = {
    'nombre': 'Isabel',
    'edad': 20,
}

print("La edad ahora es:", persona['edad'])

persona['edad'] = 25

print("La edad despues es:", persona['edad'])

persona['pais'] = 'España'

print(persona['pais'])

print("Diccionario completo:", persona)